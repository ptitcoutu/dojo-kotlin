package task02.t02_functions;

public class SolutionBox {
    int width;
    int length;
    int height;

    public SolutionBox(int width, int length, int height){
        this.width = width;
        this.length = length;
        this.height = height;
    }
    public SolutionBox( int height){
        this.width = 10;
        this.length = 10;
        this.height = height;
    }
}
